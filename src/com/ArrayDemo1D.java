package com;

public class ArrayDemo1D {
    public static void main(String[] args) {
        int nums[] = new int[4];

        nums[0] = 5;
        nums[1] = 10;
        nums[2] = 15;
        nums[3] = 20;

        for (int i = 0; i <= 4; i++) {
            System.out.println(nums[i]);
        }
    }
}
